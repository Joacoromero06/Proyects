{-
En haskell si existen NOMBRES e IDENTIFICADORES. Solo se asocia un valor.
En Haskell sí existen nombres o identificadores a los que se les asocia un valor o una función, pero no son variables mutables como en C, Java o Python.

En un lenguaje imperativo, una variable es una celda de memoria que puede cambiar de contenido.

En Haskell, un nombre es simplemente una ligadura (binding): se asocia de manera inmutable a un valor o función.
-}